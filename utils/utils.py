def plot_confusion_matrix(cm, class_names):
  """
  Returns a matplotlib figure containing the plotted confusion matrix.

  Args:
    cm (array, shape = [n, n]): a confusion matrix of integer classes
    class_names (array, shape = [n]): String names of the integer classes
  """
  cm = cm.numpy()
  print(class_names)
  figure = plt.figure(figsize=(8, 8))
  plt.imshow(cm, interpolation='nearest', cmap=plt.cm.Blues)
  plt.title("Confusion matrix")
  plt.colorbar()
  tick_marks = np.arange(len(class_names))
  plt.xticks(tick_marks, class_names, rotation=45)
  plt.yticks(tick_marks, class_names)

  # Compute the labels from the normalized confusion matrix.
  labels = np.around(cm / cm.sum(axis=1)[:, np.newaxis], decimals=2)

  # Use white text if squares are dark; otherwise black.
  threshold = cm.max() / 2.
  for i, j in product(range(cm.shape[0]), range(cm.shape[1])):
    color = "white" if cm[i, j] > threshold else "black"
    plt.text(j, i, labels[i, j], horizontalalignment="center", color=color)

  plt.tight_layout()
  plt.ylabel('True label')
  plt.xlabel('Predicted label')
  return figure


def sample_frames(sampling_mode, start_frame, end_frame, num_frames):
    if(sampling_mode == "random"):
        frame_ids = random.sample(range(start_frame, end_frame), num_frames)
        frame_ids.sort()
    else:
        frame_space = math.floor((end_frame-start_frame)/(num_frames-1))
        frame_ids = []
        frame_ids.append(start_frame)
        new_frame = start_frame
        for j in range(num_frames-2):
            new_frame = new_frame + frame_space
            frame_ids.append(new_frame)
        frame_ids.append(end_frame)
        #print(i, " " , len(frame_ids), " " ,start_frame, " ", end_frame, " ", frame_ids)
    return frame_ids

